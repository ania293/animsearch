       <h2>Czym jest wirtualna adopcja?</h2>

Jeżeli marzysz o adopcji zwierzaka, lecz nie masz możliwości tego zrobić, możesz zostać wirtualnym opiekunem jednego z naszych podopiecznych.

Na naszej stronie internetowej, przy zdjęciach wybranych zwierząt pojawią się ikonki „Czeka na wirtualnego opiekuna”. Gdy zaadoptujesz wirtualnie dane zwierzę przy jego zdjęciu pojawi się Twoje imię, nazwisko, lub nick. Będziesz mieć możliwość umieszczenia na swojej stronie internetowej, czy też blogu, zdjęć Twojego podopiecznego. Oczywiście w miarę swoich możliwości możesz również odwiedzić swojego podopiecznego, na pewno chętnie Cię pozna!

	<h2>Jakie są koszty wirtualnej adopcji?</h2>

W chwili obecnej koszt wirtualnej adopcji zwierzaka, niezależnie od jego rasy czy tez rozmiarów to 30 zł miesięcznie.

	<h2>Czy adoptując wirtualnie zwierzę podejmuję jakieś zobowiązanie?</h2>

Nie. Wpłacając na nasze konto kwotę 30 zł zostajesz wirtualnym opiekunem wybranego zwierzaka na 1 miesiąc. Dokonując takiej samej wpłaty w kolejnym miesiącu przedłużasz swój status wirtualnego opiekuna. Jeżeli chcesz zrezygnować, wystarczy napisać do nas maila z informacją, że rezygnujesz z wirtualnej adopcji oraz nie wpłacać kolejnej kwoty. Jednym słowem zrezygnować z wirtualnej adopcji możesz w każdej chwili, nie podając przyczyn. Oczywiście schronisko nie zwraca wpłaconych wcześniej pieniędzy.

	<h2>Na co są przeznaczane pieniądze z wpłat?</h2>

Adopcja wirtualna oznacza darowiznę na rzecz Schroniska dla Zwierząt w Gdyni. Pieniądze z wpłat będą przeznaczone na ogólne potrzeby w schronisku związane z utrzymaniem psów, takie jak nowe boksy, budy, leki, obroże i smycze, miski itp. W związku z przepełnieniem w schronisku nie możemy przeznaczać pieniędzy na konkretne zwierzęta i traktować ich indywidualnie, jest to również działanie sprzeczne z prawem.

	<h2>Czy zwierzak adoptowany wirtualnie może zostać zaadoptowany?</h2>

Tak. Schronisko zastrzega sobie prawo do oddania wirtualnie adoptowanego zwierzaka do prawdziwej adopcji. W takim przypadku adopcja wirtualna traci ważność, wizytówka zwierzęcia zostaje przeniesiona do działu :Znalazły dom” a nick wirtualnego opiekuna usunięty. Schronisko w ciągu tygodnia od daty adopcji zobowiązuje się do poinformowania o fakcie wirtualnego opiekuna. Schronisko nie zwraca pieniędzy wpłaconych na rzecz wirtualnej adopcji. W przypadku wpłaty na zwierzaka, który w ciągu miesiąca, za który była wpłata, znajdzie nowy dom, schronisko może zaproponować w ramach tej wpłaty innego zwierzaka do adopcji wirtualnej.

	<h2>Kto może zostać wirtualnym opiekunem?</h2>

Wirtualnym opiekunem może zostać osoba prywatna, organizacja, określona grupa ludzi (np. szkoła, klasa itp.) czy też firma.

Dla firm może to być dodatkowa forma reklamy – mogą umieścić obok zdjęcia zwierzaka swoje logo, czy też odnośnik do strony internetowej.

